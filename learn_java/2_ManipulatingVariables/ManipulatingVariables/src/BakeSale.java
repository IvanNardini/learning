/*
    Compound assignment operators:
    Addition (+=)
    Subtraction (-=)
    Multiplication (*=)
    Division (/=)
    Modulo (%=)
 */

public class BakeSale {
    public static void main(String[] args){
        int numCookies = 17;
        numCookies -= 3;
        System.out.println(numCookies);
        numCookies /= 2;
        System.out.println(numCookies);
    }
}
