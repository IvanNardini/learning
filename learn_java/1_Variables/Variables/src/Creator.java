public class Creator {
    public static void main(String[] args){

        // Variable declaration is <Primitive Datatype> <Variable Name> ;
        // Assignment is <Variable Name> = <value>;
        // Declaration and Assignment is <Primitive Datatype> <Variable Name> = <value>;

        String name = "James Gosling";
        int yearCreated = 1995;
        System.out.println(name);
        System.out.println(yearCreated);
    }
}



